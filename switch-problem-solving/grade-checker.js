// 3. Grade Checker
let gradePoint = "A" ;
switch(gradePoint){
    case "A" : console.log("excellent"); break;
    case "B" : console.log("good"); break;
    case "C" : console.log("fair"); break;
    case "D" : console.log("poor"); break;
    case "F" : console.log("fail"); break;
    default : console.log("excellent"); break;
}